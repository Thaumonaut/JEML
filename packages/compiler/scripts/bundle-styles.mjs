#!/usr/bin/env node
/**
 * Reads packages/compiler/styles/jeml.css (the source of truth for the base
 * preset) and writes packages/compiler/src/compiler/preset-styles.ts,
 * exposing the CSS as a TypeScript string constant.
 *
 * Runs as a pre-step for both `npm test` and `npm run build:cli` so the
 * generated module is always in sync with the CSS file. The generated
 * file is committed to keep `vitest run` working out of a fresh checkout
 * without an explicit codegen step.
 */
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))
const root = resolve(here, '..')
const cssPath = resolve(root, 'styles/jeml.css')
const outPath = resolve(root, 'src/compiler/preset-styles.ts')

const css = readFileSync(cssPath, 'utf8')

const header = `// AUTO-GENERATED by scripts/bundle-styles.mjs from styles/jeml.css.
// Do not edit by hand — edit styles/jeml.css and re-run the script
// (or just \`npm test\` / \`npm run build:cli\`, which trigger it).
`

const body = `export const BASE_CSS = ${JSON.stringify(css)}\n`

mkdirSync(dirname(outPath), { recursive: true })
writeFileSync(outPath, header + body, 'utf8')

const sizeKb = (css.length / 1024).toFixed(1)
console.log(`bundle-styles: wrote ${outPath} (${sizeKb} KB)`)
